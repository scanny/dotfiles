-- Configuration for NeoSolarized plugin --

-- normal,  high, or low ---
vim.g.neosolarized_contrast = "normal"
-- brightness of whitespace characters when shown ---
vim.g.neosolarized_visibility = "normal"
-- vertical split bar is transparent, 1=True, 0=False ---
vim.g.neosolarized_vertSplitBgTrans = 1
-- bold, underline, and italic are enabled, 1=True, 0=False ---
vim.g.neosolarized_bold = 1
vim.g.neosolarized_underline = 1
vim.g.neosolarized_italic = 1
-- bold is displayed as brighter color ---
vim.g.neosolarized_termBoldAsBright = 1
